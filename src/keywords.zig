/// Look up a keyword by its uppercase string. Returns NoKeyword if not found.
/// The input must already be uppercase ASCII.
pub fn lookupKeyword(word: []const u8) Keyword {
    var lo: usize = 0;
    var hi: usize = ALL_KEYWORDS.len;
    while (lo < hi) {
        const mid = lo + (hi - lo) / 2;
        const cmp = std.mem.order(u8, ALL_KEYWORDS[mid].word, word);
        switch (cmp) {
            .lt => lo = mid + 1,
            .gt => hi = mid,
            .eq => return ALL_KEYWORDS[mid].kw,
        }
    }
    return .NoKeyword;
}

/// Look up a keyword from any-case input. Performs an uppercase comparison.
pub fn lookupKeywordCaseInsensitive(word: []const u8) Keyword {
    // Stack buffer for small words; heap-less for up to 64 chars
    var buf: [128]u8 = undefined;
    if (word.len > buf.len) return .NoKeyword;
    const upper = std.ascii.upperString(buf[0..word.len], word);
    return lookupKeyword(upper);
}

