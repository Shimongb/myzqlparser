test "keyword lookup - basic" {
    try std.testing.expectEqual(Keyword.SELECT, lookupKeyword("SELECT"));
    try std.testing.expectEqual(Keyword.WHERE, lookupKeyword("WHERE"));
    try std.testing.expectEqual(Keyword.FROM, lookupKeyword("FROM"));
    try std.testing.expectEqual(Keyword.INSERT, lookupKeyword("INSERT"));
    try std.testing.expectEqual(Keyword.UPDATE, lookupKeyword("UPDATE"));
    try std.testing.expectEqual(Keyword.DELETE, lookupKeyword("DELETE"));
    try std.testing.expectEqual(Keyword.CREATE, lookupKeyword("CREATE"));
    try std.testing.expectEqual(Keyword.DROP, lookupKeyword("DROP"));
    try std.testing.expectEqual(Keyword.ALTER, lookupKeyword("ALTER"));
    try std.testing.expectEqual(Keyword.AUTO_INCREMENT, lookupKeyword("AUTO_INCREMENT"));
}

test "keyword lookup - not found" {
    try std.testing.expectEqual(Keyword.NoKeyword, lookupKeyword("FOOBAR"));
    try std.testing.expectEqual(Keyword.NoKeyword, lookupKeyword(""));
    try std.testing.expectEqual(Keyword.NoKeyword, lookupKeyword("SELECTX"));
}

test "keyword lookup - case insensitive" {
    try std.testing.expectEqual(Keyword.SELECT, lookupKeywordCaseInsensitive("select"));
    try std.testing.expectEqual(Keyword.SELECT, lookupKeywordCaseInsensitive("Select"));
    try std.testing.expectEqual(Keyword.WHERE, lookupKeywordCaseInsensitive("where"));
    try std.testing.expectEqual(Keyword.AUTO_INCREMENT, lookupKeywordCaseInsensitive("auto_increment"));
}

test "ALL_KEYWORDS is sorted" {
    var i: usize = 1;
    while (i < ALL_KEYWORDS.len) : (i += 1) {
        const prev = ALL_KEYWORDS[i - 1].word;
        const curr = ALL_KEYWORDS[i].word;
        try std.testing.expect(std.mem.lessThan(u8, prev, curr));
    }
}
